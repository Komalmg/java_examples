package TheKiranAcademy5;

public class tableof13 {
	

	    public static void main(String[] args) {
	        int number = 13; 

	        
	        for (int i = 1; i <= 10; i++) {
	            int result = number * i;
	            System.out.println(number + " * " + i + " = " + result);
	        }
	    }
	

}
